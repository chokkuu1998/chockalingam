class MultiplyDivide
{

  public static void main (String args[])
  {

    int i = 10;
    int j = 2;

    System.out.println("i is " + i);
    System.out.println("j is " + j);
  
    int k = i/j;
    System.out.println("i/j is " + k);
    k = i * j;
    System.out.println("i * j is " + k);

  }

}
